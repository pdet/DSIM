private void jPovoarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPovoarActionPerformed
    String folds = jFolds.getText();
    String diretorio = jDiretorio.getText();
    String allData = null;
    Functions weka = new Functions();
    if (buttonSelected == 1) {
        String learningRate = jTextField1.getText();
        String TrainingTime = jTextField2.getText();
        String Layers = jTextField3.getText();
        try {
            allData = weka.mlp(diretorio, learningRate, TrainingTime, Layers, folds);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(RealizarTestes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(RealizarTestes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(RealizarTestes.class.getName()).log(Level.SEVERE, null, ex);
        }

    } else if (buttonSelected == 2) {
        String clusters = jTextField4.getText();
        try {
            allData = weka.rbf(diretorio, clusters, folds);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(RealizarTestes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(RealizarTestes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(RealizarTestes.class.getName()).log(Level.SEVERE, null, ex);
        }

    } else if (buttonSelected == 3) {
        try {
            int porcentagem = Integer.parseInt(folds);
            allData = weka.regressaoLinear(diretorio,porcentagem);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(RealizarTestes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(RealizarTestes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    ResultadoTeste resultado = new ResultadoTeste();
    resultado.preencherResultado(allData);
    resultado.setVisible(true);


}//GEN-LAST:event_jPovoarActionPerformed

